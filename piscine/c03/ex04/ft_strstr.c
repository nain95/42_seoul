char	*ft_strstr(char *src, char *to_find)
{
	int		i;
	char	*s;
	char	*t;

	t = to_find;
	s = src;
	if (*t == '\0')
		return (s);
	while (*s != '\0')
	{
		if (*s == *t)
		{
			i = 1;
			while (s[i] == t[i] && t[i] != '\0')
			{
				i++;
			}
			if (t[i] == '\0')
			{
				return (s);
			}
		}
		s++;
	}
	return (0);
}
