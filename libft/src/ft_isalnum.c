int	ft_isalnum(int c)
{
	unsigned char tmp;

	tmp = c;
	if (('0' <= tmp && tmp <= '9') || ('A' <= tmp && tmp <= 'Z') ||\
			('a' <= tmp && tmp <= 'z'))
		return (1);
	return (0);
}
