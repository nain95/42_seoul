int	ft_atoi(char *str)
{
	int		answer;
	int		flag;
	char	*s;

	flag = 1;
	answer = 0;
	s = str;
	while (*s == ' ' || *s == '\t' || *s == '\n' ||\
			*s == '\v' || *s == '\r' || *s == '\f')
		s++;
	if (*s == '+' || *s == '-')
	{
		if (*s == '-')
			flag *= -1;
		s++;
	}
	while ('0' <= *s && *s <= '9')
	{
		answer *= 10;
		answer += *s++ - '0';
	}
	return (answer * flag);
}
